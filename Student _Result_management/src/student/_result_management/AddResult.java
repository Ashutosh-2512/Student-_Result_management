    private void saveStudentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveStudentActionPerformed
        // TODO add your handling code here:
        String roll = jTextPane1.getText();
        String physics = jTextPane2.getText();
        String maths = jTextPane3.getText();
        String em = jTextPane4.getText();
        String dbms = jTextPane5.getText();
        String os = jTextPane6.getText();
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root","Ashu@3269");
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno='"+roll+"'");
            if(rs.next()){
                st.executeUpdate("insert into result(rollno, physics,maths, em, dbms,os) values('"+roll+"', '"+physics+"', '"+maths+"', '"+em+"', '"+dbms+"', '"+os+"')");
                JOptionPane.showMessageDialog(null, "Marks Saved Sucessfully");
                setVisible(false);
                new AddResult().setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null, "This Roll Number is Not Registered!");
            }

        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_saveStudentActionPerformed

